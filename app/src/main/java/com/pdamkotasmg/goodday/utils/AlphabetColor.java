package com.pdamkotasmg.goodday.utils;

import com.pdamkotasmg.goodday.R;

public class AlphabetColor {
    public static int getColor(char c) {
        if (c == 'A') {
            return com.pdamkotasmg.goodday.R.color.purple_200;
        } else if (c == 'B') {
            return com.pdamkotasmg.goodday.R.color.yellowPortal;
        } else if (c == 'C') {
            return com.pdamkotasmg.goodday.R.color.primary;
        } else if (c == 'D') {
            return com.pdamkotasmg.goodday.R.color.colorPrimaryVariant;
        } else if (c == 'E') {
            return com.pdamkotasmg.goodday.R.color.teal_700;
        } else if (c == 'F') {
            return com.pdamkotasmg.goodday.R.color.teal_200;
        } else if (c == 'G') {
            return com.pdamkotasmg.goodday.R.color.grey;
        } else if (c == 'H') {
            return com.pdamkotasmg.goodday.R.color.red;
        } else if (c == 'I') {
            return com.pdamkotasmg.goodday.R.color.greenGojek;
        } else if (c == 'J') {
            return com.pdamkotasmg.goodday.R.color.orangeGreatDay;
        } else if (c == 'K') {
            return com.pdamkotasmg.goodday.R.color.orangeLightGreatDay;
        }else if (c == 'L') {
            return com.pdamkotasmg.goodday.R.color.yellowPortal;
        }else if (c == 'M') {
            return com.pdamkotasmg.goodday.R.color.redPortal;
        }else if (c == 'N') {
            return com.pdamkotasmg.goodday.R.color.greenPortal;
        }else if (c == 'O') {
            return com.pdamkotasmg.goodday.R.color.colorGold;
        }else if (c == 'P') {
            return com.pdamkotasmg.goodday.R.color.colorDarkGold;
        }else if (c == 'Q') {
            return com.pdamkotasmg.goodday.R.color.colorBlackDoff;
        }else if (c == 'R') {
            return com.pdamkotasmg.goodday.R.color.blue;
        }else if (c == 'S') {
            return com.pdamkotasmg.goodday.R.color.dash_v2_bluedark;
        }else if (c == 'T') {
            return R.color.purple_200;
        }else if (c == 'U') {
            return com.pdamkotasmg.goodday.R.color.orangeGreatDay;
        }else if (c == 'v') {
            return com.pdamkotasmg.goodday.R.color.dash_v2_azure;
        }else if (c == 'W') {
            return com.pdamkotasmg.goodday.R.color.greenPortal;
        }else if (c == 'X') {
            return com.pdamkotasmg.goodday.R.color.colorPrimaryVariant;
        }else if (c == 'Y') {
            return com.pdamkotasmg.goodday.R.color.colorGold;
        }
        // dan seterusnya hingga 'Z'
        // ...
        else {
            return com.pdamkotasmg.goodday.R.color.greenGojek;
        }
    }
}
